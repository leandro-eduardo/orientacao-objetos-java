
public class PessoaBean {
	
	// atributos encapsulados
	private String nome;
	private int idade;
	
	public PessoaBean() {
		
	}
	
	// setters e getters
	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getIdade() {
		return idade;
	}

	public void setIdade(int idade) {
		this.idade = idade;
	}
		
}
